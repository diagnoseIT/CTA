package rocks.cta.api.core.callables;

/**
 * Enumeration of possible HTTP methods.
 * 
 * @author Alexander Wert
 *
 */
public enum HTTPMethod {
	/**
	 * HTTP GET.
	 */
	GET,
	/**
	 * HTTP HEAD.
	 */
	HEAD,
	/**
	 * HTTP POST.
	 */
	POST,
	/**
	 * HTTP PUT.
	 */
	PUT,
	/**
	 * HTTP DELETE.
	 */
	DELETE,
	/**
	 * HTTP TRACE.
	 */
	TRACE,
	/**
	 * HTTP OPTIONS.
	 */
	OPTIONS,
	/**
	 * HTTP CONNECT.
	 */
	CONNECT,
	/**
	 * HTTP PATCH.
	 */
	PATCH,
	/**
	 * UNKNOWN.
	 */
	UNKNOWN
}
